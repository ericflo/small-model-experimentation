#!/usr/bin/env python3
"""Does calibration SURVIVE banking? Two passes per model:
(1) FIXED judge set (default): the BASE model's no-think samples on frozen held-out eval tasks, oracle-graded
    once (scoring only). Every model judges the SAME candidates -> any AUROC change is a change in the JUDGE.
(2) SELF-distribution (--self-dist): the model generates its OWN think-mode candidates on the eval tasks
    (the round-2 harvest distribution) and judges THEM -- the number the flywheel branch condition needs.
Inflation headline = mean_p_true_incorrect drift (overall mean drift confounds inflation with real ability)."""
from __future__ import annotations

import argparse
import json
import sys
import time
from concurrent.futures import ThreadPoolExecutor
from pathlib import Path

EXP = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(EXP / "scripts"))
sys.path.insert(0, str(EXP / "src"))
import common as C  # noqa: E402
import families as FAM  # noqa: E402
import code_env as E  # noqa: E402
from harvest_pool import auroc  # noqa: E402


def rnd(v, d=3):
    return None if v is None else round(v, d)


def extract(p, rep, gens, tasks, K):
    cands, seen = [], set()
    for ti in range(len(tasks)):
        for j in range(K):
            idx = ti * K + j
            txt = p.tok.decode(gens[idx]["seq_ids"][len(p._ids(rep[idx])):], skip_special_tokens=False)
            txt = txt.split("</think>")[-1] if "</think>" in txt else txt
            c, _ = E.extract_candidate_code(txt, "transform")
            if c and (ti, c) not in seen:
                seen.add((ti, c))
                cands.append({"ti": ti, "depth": tasks[ti]["depth"], "code": c})
    with ThreadPoolExecutor(max_workers=16) as ex:
        graded = list(ex.map(lambda x: C.grade(x["code"], tasks[x["ti"]]), cands))
    for x, g in zip(cands, graded):
        x["full_pass"] = bool(g[1])  # oracle: SCORING only
    return cands


def stats(tag, cands, pts):
    ys = [int(x["full_pass"]) for x in cands]
    by_task = {}
    for x, s, y in zip(cands, pts, ys):
        by_task.setdefault(x["ti"], []).append((s, y))
    within = []
    for sy in by_task.values():
        yy = [y for _, y in sy]
        if 0 < sum(yy) < len(yy):
            a = auroc([s for s, _ in sy], yy)
            if a is not None:
                within.append(a)
    n1 = sum(ys)
    return {"tag": tag, "n_candidates": len(cands), "n_correct": n1,
            "auroc_pooled": rnd(auroc(pts, ys)),
            "auroc_within_mean": rnd(sum(within) / len(within)) if within else None,
            "n_mixed_tasks": len(within),
            "mean_p_true": rnd(sum(pts) / max(1, len(pts))),
            "mean_p_true_correct": rnd(sum(s for s, y in zip(pts, ys) if y) / n1) if n1 else None,
            "mean_p_true_incorrect": rnd(sum(s for s, y in zip(pts, ys) if not y) / (len(ys) - n1))
            if len(ys) > n1 else None}


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--tag", type=str, required=True)
    ap.add_argument("--adapter", type=str, default=None)
    ap.add_argument("--K", type=int, default=12)
    ap.add_argument("--eval-file", type=str, default="data/eval_frozen.jsonl")
    ap.add_argument("--judge-set", type=str, default="data/judge_set.jsonl")
    ap.add_argument("--self-dist", action="store_true",
                    help="generate + judge THIS model's own think-mode candidates (round-2 distribution)")
    ap.add_argument("--think-judge", action="store_true",
                    help="judge with CoT (the working filter on this substrate) instead of no-think")
    ap.add_argument("--self-K", type=int, default=8)
    ap.add_argument("--budget", type=int, default=512)
    args = ap.parse_args()

    fam = FAM.FAMILIES["list"]
    tasks = [json.loads(l) for l in (EXP / args.eval_file).read_text().splitlines() if l.strip()]

    import gen_lib as GL
    p = GL.Probe()
    js_path = EXP / args.judge_set
    if not args.self_dist and not js_path.exists():
        assert args.adapter is None, "judge set must be generated by the BASE model first (run base calib first)"
        t0 = time.time()
        prompts = [p.prompt(C.ident_prompt(fam, t), enable_thinking=False) for t in tasks]
        rep = [pr for pr in prompts for _ in range(args.K)]
        gens = p.gen_sequences(rep, think=False, budget=0, greedy=False, answer_max=420, batch_size=64)
        cands = extract(p, rep, gens, tasks, args.K)
        js_path.write_text("\n".join(json.dumps(x) for x in cands) + "\n")
        print(f"[calib] built judge set: {len(cands)} candidates, pass-rate "
              f"{sum(x['full_pass'] for x in cands)/max(1,len(cands)):.2f} [{time.time()-t0:.0f}s]", flush=True)

    if args.adapter:
        from peft import PeftModel
        p.model = PeftModel.from_pretrained(p.model, args.adapter)
        p.model.eval()
        print(f"[calib] loaded adapter {args.adapter}", flush=True)

    if args.self_dist:
        t0 = time.time()
        prompts = [p.prompt(C.ident_prompt(fam, t), enable_thinking=True) for t in tasks]
        rep = [pr for pr in prompts for _ in range(args.self_K)]
        gens = p.gen_sequences(rep, think=True, budget=args.budget, greedy=False, answer_max=420, batch_size=48)
        cands = extract(p, rep, gens, tasks, args.self_K)
        print(f"[calib:self] {len(cands)} own candidates, pass-rate "
              f"{sum(x['full_pass'] for x in cands)/max(1,len(cands)):.2f} [{time.time()-t0:.0f}s]", flush=True)
        out_name = f"calib_self_{args.tag}.json"
    else:
        cands = [json.loads(l) for l in js_path.read_text().splitlines() if l.strip()]
        out_name = f"calib_{args.tag}.json"
    if args.think_judge:
        out_name = out_name.replace("calib_", "calib_think_", 1) if not args.self_dist \
            else f"calib_self_think_{args.tag}.json"

    if not cands:
        print("[calib] no candidates; writing null stats")
        out = {"tag": args.tag, "n_candidates": 0}
    else:
        jp = [p.judge_prompt(C.ident_prompt(fam, tasks[x["ti"]]), x["code"],
                             enable_thinking=args.think_judge) for x in cands]
        if args.think_judge:
            pts, forced = p.judge_think(jp, budget=args.budget, gen_batch=32, logit_batch=16)
            pts = [float(v) for v in pts]
        else:
            pts = p.judge_nothink(jp, batch_size=16)
        out = stats(args.tag, cands, pts)
        if args.think_judge:
            out["forced_close_rate"] = rnd(sum(forced) / max(1, len(forced)))
        if args.self_dist:
            out["self_pass_rate"] = rnd(sum(x["full_pass"] for x in cands) / len(cands))
    (EXP / "runs").mkdir(exist_ok=True)
    (EXP / "runs" / out_name).write_text(json.dumps(out, indent=1))
    print(json.dumps(out))


if __name__ == "__main__":
    main()
