def write_json(path: Path, data: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(data, indent=2, default=json_default))


def json_default(value: Any) -> Any:
    if isinstance(value, Path):
        return str(value)
    if isinstance(value, np.generic):
        return value.item()
    if isinstance(value, torch.Tensor):
        return value.detach().cpu().tolist()
    if isinstance(value, float) and (math.isnan(value) or math.isinf(value)):
        return None
    return value


def write_csv(path: Path, rows: Sequence[Dict[str, Any]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    if not rows:
        path.write_text("")
        return
    keys: List[str] = []
    for row in rows:
        for key in row:
            if key not in keys:
                keys.append(key)
    with path.open("w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=keys)
        writer.writeheader()
        writer.writerows(rows)


def append_csv(path: Path, row: Dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    exists = path.exists()
    with path.open("a" if exists else "w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=list(row.keys()))
        if not exists:
            writer.writeheader()
        writer.writerow(row)


def pct(x: float) -> str:
    if math.isnan(float(x)):
        return "n/a"
    return f"{100.0 * float(x):.1f}%"


def parse_int_list(raw: str) -> List[int]:
    return [int(x.strip()) for x in raw.split(",") if x.strip()]


def parse_str_list(raw: str) -> List[str]:
    return [x.strip() for x in raw.split(",") if x.strip()]


def ensure_pad_token(tokenizer: Any) -> None:
    if getattr(tokenizer, "pad_token_id", None) is None:
        tokenizer.pad_token = tokenizer.eos_token or tokenizer.unk_token or tokenizer.convert_ids_to_tokens(0)
    tokenizer.padding_side = "right"


def dtype_from_string(name: str) -> torch.dtype:
    name = name.lower()
    if name in {"bf16", "bfloat16"}:
        return torch.bfloat16
    if name in {"fp16", "float16", "half"}:
        return torch.float16
    if name in {"fp32", "float32"}:
        return torch.float32
    raise ValueError(name)


@dataclass
class MiniProgram:
    program: str
    answer: int
    states: List[int]
    op_names: List[str]
    length: int
    split: str
    composition: str
    example_id: str


class MiniPythonGenerator:
    def __init__(self, seed: int, value_classes: int = VALUE_CLASSES) -> None:
        self.rng = random.Random(seed)
        self.value_classes = value_classes
        self.words = ["cat", "lamp", "river", "stone", "yellow", "orbit", "tea", "violet"]
        self.colors = ["red", "blue", "green", "gold", "white"]

    def _valid(self, x: int) -> bool:
        return 0 <= int(x) < self.value_classes

    def _op_add(self, x: int) -> Optional[Tuple[str, int, str]]:
        c = self.rng.randint(1, 4)
        y = x + c
        if not self._valid(y):
            return None
        return f"x = x + {c}", y, "add"

    def _op_sub(self, x: int) -> Optional[Tuple[str, int, str]]:
        c = self.rng.randint(1, 4)
        y = x - c
        if not self._valid(y):
            return None
        return f"x = x - {c}", y, "sub"

    def _op_mul_add(self, x: int) -> Optional[Tuple[str, int, str]]:
        m = 2
        c = self.rng.randint(0, 2)
        y = x * m + c
        if not self._valid(y):
            return None
        return f"x = x * {m} + {c}", y, "mul_add"

    def _op_half_add(self, x: int) -> Optional[Tuple[str, int, str]]:
        c = self.rng.randint(0, 4)
        y = x // 2 + c
        if not self._valid(y):
            return None
        return f"x = x // 2 + {c}", y, "half_add"

    def _op_str_len(self, x: int) -> Optional[Tuple[str, int, str]]:
        word = self.rng.choice(self.words)
        y = x + len(word)
        if not self._valid(y):
            return None
        return f'x = x + len("{word}")', y, "str_len"

    def _op_reverse_len(self, x: int) -> Optional[Tuple[str, int, str]]:
        word = self.rng.choice(self.words)
        y = x + len(word[::-1])
        if not self._valid(y):
            return None
        return f'x = x + len("{word}"[::-1])', y, "reverse_len"

    def _op_list_index(self, x: int) -> Optional[Tuple[str, int, str]]:
        target = self.rng.choice(self.colors)
        idx = self.colors.index(target)
        y = x + idx
        if not self._valid(y):
            return None
        items = ", ".join(f'"{c}"' for c in self.colors)
        return f'x = x + [{items}].index("{target}")', y, "list_index"

    def _op_list_len(self, x: int) -> Optional[Tuple[str, int, str]]:
        n = self.rng.randint(2, 5)
        vals = [self.rng.randint(0, 9) for _ in range(n)]
        y = x + len(vals)
        if not self._valid(y):
            return None
        return f"x = x + len({vals})", y, "list_len"

    def _op_branch(self, x: int) -> Optional[Tuple[str, int, str]]:
        threshold = self.rng.randint(8, 24)
        down = self.rng.randint(1, 4)
        up = self.rng.randint(1, 4)
        y = x - down if x > threshold else x + up
        if not self._valid(y):
            return None
        line = f"if x > {threshold}:\n    x = x - {down}\nelse:\n    x = x + {up}"
        return line, y, "branch"

    def _op_min_cap(self, x: int) -> Optional[Tuple[str, int, str]]:
        c = self.rng.randint(1, 4)
        cap = self.rng.randint(12, 30)
        y = min(x + c, cap)
        if not self._valid(y):
            return None
        return f"x = min(x + {c}, {cap})", y, "min_cap"

    def _op_max_floor(self, x: int) -> Optional[Tuple[str, int, str]]:
        floor = self.rng.randint(0, 16)
        y = max(x - self.rng.randint(0, 3), floor)
        if not self._valid(y):
            return None
        return f"x = max(x - 0, {floor})" if y == max(x, floor) else f"x = max(x - {max(0, x - y)}, {floor})", y, "max_floor"

    def _sample_op(self, x: int, allowed: Optional[Sequence[str]] = None) -> Optional[Tuple[str, int, str]]:
        ops = {
            "add": self._op_add,
            "sub": self._op_sub,
            "mul_add": self._op_mul_add,
            "half_add": self._op_half_add,
            "str_len": self._op_str_len,
            "reverse_len": self._op_reverse_len,
            "list_index": self._op_list_index,
            "list_len": self._op_list_len,
            "branch": self._op_branch,
            "min_cap": self._op_min_cap,
            "max_floor": self._op_max_floor,
        }
        names = list(allowed) if allowed else list(ops)
        self.rng.shuffle(names)
        for name in names:
            out = ops[name](x)
            if out is not None:
                return out
        return None

    def make_one(self, length: int, split: str, require_heldout_bigram: bool = False, forbid_heldout_bigram: bool = True) -> MiniProgram:
        for attempt in range(2000):
            x = self.rng.randint(0, 12)
            lines = [f"x = {x}"]
            states: List[int] = []
            names: List[str] = []
            forced_insert = require_heldout_bigram and length >= 2
            forced_at = self.rng.randint(0, max(0, length - 2)) if forced_insert else -1
            for step in range(length):
                allowed = None
                if forced_insert and step == forced_at:
                    allowed = [self.rng.choice(list(HELDOUT_BIGRAMS))[0]]
                elif forced_insert and step == forced_at + 1:
                    prev = names[-1]
                    nexts = [b for a, b in HELDOUT_BIGRAMS if a == prev]
                    allowed = nexts or None
                out = self._sample_op(x, allowed=allowed)
                if out is None:
                    break
                line, y, name = out
                if forbid_heldout_bigram and names and (names[-1], name) in HELDOUT_BIGRAMS:
                    break
                lines.append(line)
                names.append(name)
                x = y
                states.append(x)
            if len(states) != length:
                continue
            has_heldout = any((a, b) in HELDOUT_BIGRAMS for a, b in zip(names, names[1:]))
            if require_heldout_bigram and not has_heldout:
                continue
            if forbid_heldout_bigram and has_heldout:
                continue
            lines.append("answer = x")
            program = "\n".join(lines)
            comp = "heldout_composition" if has_heldout else "in_distribution_composition"
            eid = f"{split}_{length}_{attempt}_{abs(hash(program)) % 10_000_000}"
            return MiniProgram(program=program, answer=x, states=states, op_names=names, length=length, split=split, composition=comp, example_id=eid)
        raise RuntimeError(f"could not generate program length={length} split={split}")

    def make_set(self, n: int, lengths: Sequence[int], split: str, require_heldout_bigram: bool = False, forbid_heldout_bigram: bool = True) -> List[MiniProgram]:
        out = []
        for i in range(n):
            length = lengths[i % len(lengths)]
            out.append(self.make_one(length, split, require_heldout_bigram=require_heldout_bigram, forbid_heldout_bigram=forbid_heldout_bigram))
        self.rng.shuffle(out)
        return out


