def markdown_table(df: pd.DataFrame, columns: Sequence[str], max_rows: int = 40) -> str:
    if df.empty:
        return "_No rows._"
    sub = df.loc[:, [c for c in columns if c in df.columns]].head(max_rows).copy()
    for c in sub.columns:
        if c.endswith("accuracy") or c in {"final_accuracy", "state_accuracy"}:
            sub[c] = sub[c].map(lambda x: pct(x) if pd.notna(x) else "n/a")
        elif c == "mean_output_tokens":
            sub[c] = sub[c].map(lambda x: f"{float(x):.1f}" if pd.notna(x) else "n/a")
    return sub.to_markdown(index=False)


