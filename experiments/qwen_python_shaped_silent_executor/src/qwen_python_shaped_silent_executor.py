def run(args: argparse.Namespace) -> None:
    start = time.time()
    run_dir = RUNS / args.run_name
    run_dir.mkdir(parents=True, exist_ok=True)
    REPORTS.mkdir(parents=True, exist_ok=True)
    FIGURES.mkdir(parents=True, exist_ok=True)
    CHECKPOINTS.mkdir(parents=True, exist_ok=True)
    CACHES.mkdir(parents=True, exist_ok=True)
    if args.analyze_only:
        config_path = run_dir / "run_config.json"
        if config_path.exists():
            saved = json.loads(config_path.read_text())
            for key, value in saved.items():
                if hasattr(args, key):
                    setattr(args, key, value)
            args.analyze_only = True
            args.train_lengths = parse_int_list(getattr(args, "train_lengths_raw", "4,8,16"))
        build_report(args.run_name, args)
        return
    write_json(run_dir / "run_config.json", vars(args))
    set_seed(args.data_seed)
    splits = make_splits(args)
    save_dataset_manifest(args.run_name, splits)
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")

    tokenizer, frozen_model, _ = load_qwen(args, train_lora=False)
    run_frozen_probe(args.run_name, frozen_model, tokenizer, splits, args, device)
    del frozen_model
    gc.collect()
    if torch.cuda.is_available():
        torch.cuda.empty_cache()

    all_train_rows: List[Dict[str, Any]] = []
    all_metrics: List[Dict[str, Any]] = []
    for seed in parse_int_list(args.train_seeds):
        log(f"[main] training ordered silent executor seed={seed}")
        tokenizer, model, hidden_size = load_qwen(args, train_lora=True)
        ckpt, rows = train_silent_executor(args.run_name, seed, model, tokenizer, hidden_size, splits, args, device, shuffle_train=False)
        all_train_rows.extend(rows)
        all_metrics.extend(evaluate_all_trained(args.run_name, seed, model, tokenizer, hidden_size, ckpt, splits, args, device, train_shuffle=False))
        del model
        gc.collect()
        if torch.cuda.is_available():
            torch.cuda.empty_cache()
    write_csv(run_dir / "all_trained_metrics.csv", all_metrics)
    build_report(args.run_name, args)
    write_json(
        run_dir / "run_summary.json",
        {
            "elapsed_sec": time.time() - start,
            "train_rows": len(all_train_rows),
            "trained_metric_rows": len(all_metrics),
            "splits": {k: len(v) for k, v in splits.items()},
        },
    )


