def collate_prompts(
    examples: Sequence[MiniProgram],
    tokenizer: Any,
    max_prompt_len: int,
    device: torch.device,
    prompt_fn=silent_prompt,
) -> Dict[str, torch.Tensor]:
    enc = tokenizer(
        [format_instruction(tokenizer, prompt_fn(ex), SILENT_SYSTEM) for ex in examples],
        padding=True,
        truncation=True,
        max_length=max_prompt_len,
        return_tensors="pt",
    )
    max_states = max(len(ex.states) for ex in examples)
    state_targets = torch.full((len(examples), max_states), -100, dtype=torch.long)
    for i, ex in enumerate(examples):
        state_targets[i, : len(ex.states)] = torch.tensor(ex.states, dtype=torch.long)
    return {
        "input_ids": enc["input_ids"].to(device),
        "attention_mask": enc["attention_mask"].to(device),
        "answers": torch.tensor([ex.answer for ex in examples], dtype=torch.long, device=device),
        "state_targets": state_targets.to(device),
        "lengths": torch.tensor([ex.length for ex in examples], dtype=torch.long, device=device),
    }


def sample_examples(examples: Sequence[MiniProgram], batch_size: int) -> List[MiniProgram]:
    if len(examples) >= batch_size:
        return random.sample(list(examples), batch_size)
    return [random.choice(list(examples)) for _ in range(batch_size)]


class PromptDirectHead(nn.Module):
    def __init__(self, hidden_size: int, width: int, dropout: float) -> None:
        super().__init__()
        self.net = nn.Sequential(
            nn.LayerNorm(hidden_size),
            nn.Linear(hidden_size, width),
            nn.GELU(),
            nn.Dropout(dropout),
            nn.Linear(width, VALUE_CLASSES),
        )

    def forward(self, hidden: torch.Tensor, attention_mask: torch.Tensor) -> torch.Tensor:
        mask = attention_mask.float()
        pooled = (hidden.float() * mask.unsqueeze(-1)).sum(dim=1) / mask.sum(dim=1).clamp_min(1).unsqueeze(-1)
        return self.net(pooled)


class LatentExecutorHead(nn.Module):
    def __init__(self, hidden_size: int, width: int, dropout: float) -> None:
        super().__init__()
        self.state = nn.Sequential(
            nn.LayerNorm(hidden_size),
            nn.Linear(hidden_size, width),
            nn.GELU(),
            nn.Dropout(dropout),
            nn.Linear(width, VALUE_CLASSES),
        )
        self.final = nn.Sequential(
            nn.LayerNorm(hidden_size),
            nn.Linear(hidden_size, width),
            nn.GELU(),
            nn.Dropout(dropout),
            nn.Linear(width, VALUE_CLASSES),
        )

    def forward(self, latent_hidden: torch.Tensor) -> Dict[str, torch.Tensor]:
        return {
            "state_logits": self.state(latent_hidden.float()),
            "final_logits": self.final(latent_hidden[:, -1, :].float()),
        }


def extract_hidden(outputs: Any) -> torch.Tensor:
    hidden_states = getattr(outputs, "hidden_states", None)
    if hidden_states is not None:
        return hidden_states[-1]
    last = getattr(outputs, "last_hidden_state", None)
    if torch.is_tensor(last):
        return last
    raise RuntimeError("model did not return hidden states")


def forward_prompt_hidden(model: nn.Module, batch: Dict[str, torch.Tensor]) -> torch.Tensor:
    outputs = model(
        input_ids=batch["input_ids"],
        attention_mask=batch["attention_mask"],
        output_hidden_states=True,
        use_cache=False,
        return_dict=True,
    )
    return extract_hidden(outputs)


def forward_latent_hidden(
    model: nn.Module,
    batch: Dict[str, torch.Tensor],
    latent_table: nn.Parameter,
    k: int,
    shuffle_latents: bool,
) -> torch.Tensor:
    embed = model.get_input_embeddings()
    prompt_embeds = embed(batch["input_ids"])
    if k <= 0:
        return forward_prompt_hidden(model, batch)
    bsz = prompt_embeds.shape[0]
    indices = torch.arange(k, device=prompt_embeds.device)
    if shuffle_latents:
        perm = torch.randperm(k, device=prompt_embeds.device)
        indices = indices[perm]
    latent = latent_table[indices].unsqueeze(0).expand(bsz, -1, -1).to(prompt_embeds.dtype)
    inputs_embeds = torch.cat([prompt_embeds, latent], dim=1)
    attn = torch.cat(
        [batch["attention_mask"], torch.ones((bsz, k), dtype=batch["attention_mask"].dtype, device=batch["attention_mask"].device)],
        dim=1,
    )
    outputs = model(
        inputs_embeds=inputs_embeds,
        attention_mask=attn,
        output_hidden_states=True,
        use_cache=False,
        return_dict=True,
    )
    return extract_hidden(outputs)[:, -k:, :]


def load_qwen(args: argparse.Namespace, train_lora: bool) -> Tuple[Any, nn.Module, int]:
    tokenizer = AutoTokenizer.from_pretrained(args.model_id, trust_remote_code=True, use_fast=True)
    ensure_pad_token(tokenizer)
    dtype = dtype_from_string(args.torch_dtype)
    quantization_config = None
    if args.load_in_4bit:
        quantization_config = BitsAndBytesConfig(
            load_in_4bit=True,
            bnb_4bit_quant_type="nf4",
            bnb_4bit_compute_dtype=dtype if dtype != torch.float32 else torch.bfloat16,
            bnb_4bit_use_double_quant=True,
        )
    kwargs: Dict[str, Any] = {
        "trust_remote_code": True,
        "torch_dtype": dtype,
        "low_cpu_mem_usage": True,
        "device_map": args.device_map if torch.cuda.is_available() else None,
    }
    if quantization_config is not None:
        kwargs["quantization_config"] = quantization_config
    kwargs = {k: v for k, v in kwargs.items() if v is not None}
    log(f"[load] {args.model_id} train_lora={train_lora}")
    model = AutoModelForCausalLM.from_pretrained(args.model_id, **kwargs)
    model.config.use_cache = False
    if train_lora:
        if args.gradient_checkpointing and hasattr(model, "gradient_checkpointing_enable"):
            model.gradient_checkpointing_enable()
        if args.load_in_4bit:
            model = prepare_model_for_kbit_training(model, use_gradient_checkpointing=args.gradient_checkpointing)
        targets: str | List[str]
        if args.lora_target_modules == "all-linear":
            targets = "all-linear"
        else:
            targets = parse_str_list(args.lora_target_modules)
        config = LoraConfig(
            r=args.lora_r,
            lora_alpha=args.lora_alpha,
            lora_dropout=args.lora_dropout,
            target_modules=targets,
            bias="none",
            task_type="CAUSAL_LM",
        )
        model = get_peft_model(model, config)
        model.print_trainable_parameters()
    else:
        for p in model.parameters():
            p.requires_grad_(False)
        model.eval()
    return tokenizer, model, int(model.config.hidden_size)


def optimizer_for(params: Iterable[nn.Parameter], args: argparse.Namespace) -> torch.optim.Optimizer:
    trainable = [p for p in params if p.requires_grad]
    if args.optimizer == "paged_adamw_8bit":
        try:
            import bitsandbytes as bnb  # type: ignore

            return bnb.optim.PagedAdamW8bit(trainable, lr=args.lr, weight_decay=args.weight_decay)
        except Exception as exc:
            log(f"[optim] falling back to AdamW: {exc}")
    return torch.optim.AdamW(trainable, lr=args.lr, weight_decay=args.weight_decay)


@torch.no_grad()
def score_numeric_continuations(
    model: nn.Module,
    tokenizer: Any,
    prefixes: Sequence[str],
    values: Sequence[int],
    device: torch.device,
    batch_size: int,
    max_length: int,
) -> np.ndarray:
    model.eval()
    scores = np.full((len(prefixes), len(values)), -1e9, dtype=np.float32)
    texts: List[Tuple[int, int, List[int], List[int]]] = []
    for i, prefix in enumerate(prefixes):
        prefix_ids = tokenizer(prefix, add_special_tokens=False)["input_ids"]
        for j, value in enumerate(values):
            cand_ids = tokenizer(" " + str(value), add_special_tokens=False)["input_ids"]
            ids = prefix_ids + cand_ids
            if len(ids) <= max_length and prefix_ids and cand_ids:
                texts.append((i, j, ids, cand_ids))
    for start in range(0, len(texts), batch_size):
        chunk = texts[start : start + batch_size]
        max_len = max(len(ids) for _, _, ids, _ in chunk)
        input_ids = torch.full((len(chunk), max_len), tokenizer.pad_token_id, dtype=torch.long, device=device)
        attn = torch.zeros((len(chunk), max_len), dtype=torch.long, device=device)
        for r, (_, _, ids, _) in enumerate(chunk):
            input_ids[r, : len(ids)] = torch.tensor(ids, dtype=torch.long, device=device)
            attn[r, : len(ids)] = 1
        out = model(input_ids=input_ids, attention_mask=attn, use_cache=False, return_dict=True)
        logp = F.log_softmax(out.logits[:, :-1, :].float(), dim=-1)
        for r, (i, j, ids, cand_ids) in enumerate(chunk):
            prefix_len = len(ids) - len(cand_ids)
            total = 0.0
            for t, tok in enumerate(cand_ids):
                pos = prefix_len + t - 1
                if pos < 0 or pos >= logp.shape[1]:
                    total = -1e9
                    break
                total += float(logp[r, pos, tok].detach().cpu())
            scores[i, j] = total
    return scores


def parse_cot_output(text: str) -> Tuple[List[int], Optional[int]]:
    states: List[int] = []
    answer: Optional[int] = None
    state_match = re.search(r"states\s*:\s*\[([^\]]*)\]", text, flags=re.IGNORECASE)
    if state_match:
        states = [int(x) for x in re.findall(r"-?\d+", state_match.group(1))]
    ans_match = re.search(r"answer\s*:\s*(-?\d+)", text, flags=re.IGNORECASE)
    if ans_match:
        answer = int(ans_match.group(1))
    else:
        nums = re.findall(r"-?\d+", text)
        if nums:
            answer = int(nums[-1])
    return states, answer


@torch.no_grad()
def generate_cot(
    model: nn.Module,
    tokenizer: Any,
    examples: Sequence[MiniProgram],
    device: torch.device,
    max_new_tokens: int,
    cot_thinking: bool,
) -> List[Dict[str, Any]]:
    rows: List[Dict[str, Any]] = []
    model.eval()
    for idx, ex in enumerate(examples):
        prompt = format_instruction(tokenizer, cot_prompt(ex), COT_SYSTEM, enable_thinking=cot_thinking)
        enc = tokenizer(prompt, return_tensors="pt", truncation=True, max_length=1536).to(device)
        out = model.generate(
            **enc,
            do_sample=False,
            max_new_tokens=max_new_tokens,
            pad_token_id=tokenizer.pad_token_id,
            eos_token_id=tokenizer.eos_token_id,
        )
        gen_ids = out[0, enc["input_ids"].shape[1] :]
        text = tokenizer.decode(gen_ids, skip_special_tokens=True)
        pred_states, pred_answer = parse_cot_output(text)
        state_match = 0
        denom = max(len(ex.states), 1)
        for a, b in zip(pred_states, ex.states):
            state_match += int(a == b)
        rows.append(
            {
                "example_id": ex.example_id,
                "split": ex.split,
                "length": ex.length,
                "answer": ex.answer,
                "pred_answer": pred_answer if pred_answer is not None else "",
                "final_correct": int(pred_answer == ex.answer),
                "state_accuracy": state_match / denom,
                "generated_tokens": int(gen_ids.numel()),
                "raw_output": text[:500],
            }
        )
        if (idx + 1) % 8 == 0:
            log(f"[cot] generated {idx + 1}/{len(examples)}")
    return rows


def latent_targets_for_k(examples: Sequence[MiniProgram], k: int, device: torch.device) -> torch.Tensor:
    targets = torch.full((len(examples), k), -100, dtype=torch.long, device=device)
    for i, ex in enumerate(examples):
        if k <= 0:
            continue
        vals = list(ex.states[:k])
        if len(vals) < k:
            vals.extend([ex.answer] * (k - len(vals)))
        targets[i] = torch.tensor(vals[:k], dtype=torch.long, device=device)
    return targets


def train_silent_executor(
    run_name: str,
    seed: int,
    model: nn.Module,
    tokenizer: Any,
    hidden_size: int,
    splits: Dict[str, List[MiniProgram]],
    args: argparse.Namespace,
    device: torch.device,
    shuffle_train: bool,
) -> Tuple[Path, List[Dict[str, Any]]]:
    set_seed(seed)
    direct_head = PromptDirectHead(hidden_size, args.head_width, args.dropout).to(device)
    latent_head = LatentExecutorHead(hidden_size, args.head_width, args.dropout).to(device)
    base_embed = model.get_input_embeddings().weight[int(tokenizer.eos_token_id or 0)].detach().float()
    latent_table = nn.Parameter(base_embed.unsqueeze(0).repeat(args.max_latents, 1) + 0.01 * torch.randn(args.max_latents, hidden_size, device=base_embed.device))
    latent_table = nn.Parameter(latent_table.to(device))
    params = list(model.parameters()) + list(direct_head.parameters()) + list(latent_head.parameters()) + [latent_table]
    opt = optimizer_for(params, args)
    train_rows: List[Dict[str, Any]] = []
    train_k = parse_int_list(args.train_k)
    for step in range(1, args.train_steps + 1):
        model.train()
        direct_head.train()
        latent_head.train()
        examples = sample_examples(splits["train"], args.batch_size)
        k = random.choice(train_k)
        batch = collate_prompts(examples, tokenizer, args.max_prompt_len, device)
        if k <= 0:
            hidden = forward_prompt_hidden(model, batch)
            logits = direct_head(hidden, batch["attention_mask"])
            final_loss = F.cross_entropy(logits, batch["answers"])
            state_loss = torch.tensor(0.0, device=device)
            loss = final_loss
        else:
            latent_hidden = forward_latent_hidden(model, batch, latent_table, k, shuffle_latents=shuffle_train)
            out = latent_head(latent_hidden)
            final_loss = F.cross_entropy(out["final_logits"], batch["answers"])
            state_targets = latent_targets_for_k(examples, k, device)
            state_loss = F.cross_entropy(out["state_logits"].reshape(-1, VALUE_CLASSES), state_targets.reshape(-1), ignore_index=-100)
            loss = final_loss + args.state_loss_weight * state_loss
        opt.zero_grad(set_to_none=True)
        loss.backward()
        torch.nn.utils.clip_grad_norm_([p for p in params if p.requires_grad], args.grad_clip)
        opt.step()
        if step == 1 or step == args.train_steps or step % args.eval_every == 0:
            quick = evaluate_trained(
                model,
                tokenizer,
                direct_head,
                latent_head,
                latent_table,
                splits["val"][: min(args.quick_eval_size, len(splits["val"]))],
                [0, min(8, args.max_latents), min(16, args.max_latents)],
                args,
                device,
                seed,
                "val_quick",
                shuffle_eval=False,
            )
            best_quick = max(r["final_accuracy"] for r in quick)
            row = {
                "run": run_name,
                "seed": seed,
                "step": step,
                "k": k,
                "shuffle_train": int(shuffle_train),
                "loss": float(loss.detach().cpu()),
                "final_loss": float(final_loss.detach().cpu()),
                "state_loss": float(state_loss.detach().cpu()),
                "quick_best_final_accuracy": best_quick,
            }
            train_rows.append(row)
            append_csv(RUNS / run_name / "training_log.csv", row)
            log(f"[train {run_name} seed={seed}] step {step}/{args.train_steps} loss={row['loss']:.3f} quick_best={100*best_quick:.1f}%")
    ckpt_dir = CHECKPOINTS / run_name / f"seed{seed}_{'shuffle' if shuffle_train else 'ordered'}"
    ckpt_dir.mkdir(parents=True, exist_ok=True)
    if hasattr(model, "save_pretrained"):
        model.save_pretrained(ckpt_dir / "adapter")
    torch.save(
        {
            "direct_head": direct_head.state_dict(),
            "latent_head": latent_head.state_dict(),
            "latent_table": latent_table.detach().cpu(),
            "hidden_size": hidden_size,
            "args": vars(args),
        },
        ckpt_dir / "heads.pt",
    )
    return ckpt_dir, train_rows


@torch.no_grad()
def evaluate_trained(
    model: nn.Module,
    tokenizer: Any,
    direct_head: PromptDirectHead,
    latent_head: LatentExecutorHead,
    latent_table: nn.Parameter,
    examples: Sequence[MiniProgram],
    k_values: Sequence[int],
    args: argparse.Namespace,
    device: torch.device,
    seed: int,
    split: str,
    shuffle_eval: bool,
) -> List[Dict[str, Any]]:
    model.eval()
    direct_head.eval()
    latent_head.eval()
    rows: List[Dict[str, Any]] = []
    for k in k_values:
        preds: List[int] = []
        answers: List[int] = []
        state_accs: List[float] = []
        for start in range(0, len(examples), args.eval_batch_size):
            batch_examples = examples[start : start + args.eval_batch_size]
            batch = collate_prompts(batch_examples, tokenizer, args.max_prompt_len, device)
            if k <= 0:
                hidden = forward_prompt_hidden(model, batch)
                logits = direct_head(hidden, batch["attention_mask"])
                pred = logits.argmax(dim=-1)
                preds.extend(pred.detach().cpu().tolist())
                answers.extend(batch["answers"].detach().cpu().tolist())
                state_accs.extend([float("nan")] * len(batch_examples))
            else:
                latent_hidden = forward_latent_hidden(model, batch, latent_table, k, shuffle_latents=shuffle_eval)
                out = latent_head(latent_hidden)
                pred = out["final_logits"].argmax(dim=-1)
                state_pred = out["state_logits"].argmax(dim=-1).detach().cpu().tolist()
                preds.extend(pred.detach().cpu().tolist())
                answers.extend(batch["answers"].detach().cpu().tolist())
                for ex, sp in zip(batch_examples, state_pred):
                    denom = max(1, min(len(ex.states), k))
                    state_accs.append(sum(int(a == b) for a, b in zip(sp[:denom], ex.states[:denom])) / denom)
        acc = accuracy_score(answers, preds) if answers else float("nan")
        finite_state = [x for x in state_accs if not math.isnan(float(x))]
        rows.append(
            {
                "arm": "silent_shuffled" if shuffle_eval and k > 0 else ("trained_direct" if k == 0 else "silent_ordered"),
                "seed": seed,
                "split": split,
                "k": k,
                "n": len(examples),
                "final_accuracy": float(acc),
                "state_accuracy": float(np.mean(finite_state)) if finite_state else float("nan"),
                "mean_output_tokens": 1.0,
            }
        )
    return rows


def run_frozen_probe(
    run_name: str,
    model: nn.Module,
    tokenizer: Any,
    splits: Dict[str, List[MiniProgram]],
    args: argparse.Namespace,
    device: torch.device,
) -> Tuple[List[Dict[str, Any]], List[Dict[str, Any]]]:
    values = list(range(VALUE_CLASSES))
    probe_rows: List[Dict[str, Any]] = []
    cot_rows: List[Dict[str, Any]] = []
    probe_splits = ["eval_len4", "eval_len8", "eval_len16", "eval_len24", "eval_len32", "eval_comp16", "eval_comp24"]
    for split in probe_splits:
        examples = splits[split][: args.probe_eval_size]
        prefixes = [format_instruction(tokenizer, direct_prompt(ex), DIRECT_SYSTEM) for ex in examples]
        log(f"[probe] frozen direct scoring {split} n={len(examples)}")
        scores = score_numeric_continuations(model, tokenizer, prefixes, values, device, args.score_batch_size, args.max_score_len)
        preds = scores.argmax(axis=1).tolist()
        acc = accuracy_score([ex.answer for ex in examples], preds)
        probe_rows.append(
            {
                "arm": "frozen_direct_likelihood",
                "seed": -1,
                "split": split,
                "k": 0,
                "n": len(examples),
                "final_accuracy": float(acc),
                "state_accuracy": float("nan"),
                "mean_output_tokens": 1.0,
            }
        )
    for split in ["eval_len4", "eval_len8", "eval_len16", "eval_len24"]:
        examples = splits[split][: args.cot_eval_size]
        log(f"[probe] CoT generation {split} n={len(examples)}")
        rows = generate_cot(model, tokenizer, examples, device, args.cot_max_new_tokens, args.cot_thinking)
        cot_rows.extend(rows)
        if rows:
            probe_rows.append(
                {
                    "arm": "frozen_cot_generate",
                    "seed": -1,
                    "split": split,
                    "k": -1,
                    "n": len(rows),
                    "final_accuracy": float(np.mean([r["final_correct"] for r in rows])),
                    "state_accuracy": float(np.mean([r["state_accuracy"] for r in rows])),
                    "mean_output_tokens": float(np.mean([r["generated_tokens"] for r in rows])),
                }
            )
    write_csv(RUNS / run_name / "frozen_probe_metrics.csv", probe_rows)
    write_csv(RUNS / run_name / "cot_generations.csv", cot_rows)
    return probe_rows, cot_rows


def evaluate_all_trained(
    run_name: str,
    seed: int,
    model: nn.Module,
    tokenizer: Any,
    hidden_size: int,
    ckpt_dir: Path,
    splits: Dict[str, List[MiniProgram]],
    args: argparse.Namespace,
    device: torch.device,
    train_shuffle: bool,
) -> List[Dict[str, Any]]:
    payload = torch.load(ckpt_dir / "heads.pt", map_location=device)
    direct_head = PromptDirectHead(hidden_size, args.head_width, args.dropout).to(device)
    latent_head = LatentExecutorHead(hidden_size, args.head_width, args.dropout).to(device)
    direct_head.load_state_dict(payload["direct_head"])
    latent_head.load_state_dict(payload["latent_head"])
    latent_table = nn.Parameter(payload["latent_table"].to(device))
    rows: List[Dict[str, Any]] = []
    eval_splits = ["eval_len4", "eval_len8", "eval_len16", "eval_len24", "eval_len32", "eval_comp16", "eval_comp24"]
    k_values = parse_int_list(args.eval_k)
    for split in eval_splits:
        examples = splits[split]
        rows.extend(evaluate_trained(model, tokenizer, direct_head, latent_head, latent_table, examples, k_values, args, device, seed, split, shuffle_eval=False))
        rows.extend(evaluate_trained(model, tokenizer, direct_head, latent_head, latent_table, examples, [k for k in k_values if k > 0], args, device, seed, split, shuffle_eval=True))
    for row in rows:
        row["train_shuffle"] = int(train_shuffle)
    write_csv(RUNS / run_name / f"trained_metrics_seed{seed}_{'shuffle' if train_shuffle else 'ordered'}.csv", rows)
    return rows


