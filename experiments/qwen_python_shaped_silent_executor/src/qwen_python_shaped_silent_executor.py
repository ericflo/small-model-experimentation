def collect_results(run_name: str) -> Tuple[pd.DataFrame, pd.DataFrame, pd.DataFrame]:
    run_dir = RUNS / run_name
    metric_frames = []
    has_all_trained = (run_dir / "all_trained_metrics.csv").exists()
    for path in sorted(run_dir.glob("*metrics*.csv")):
        if path.name == "cot_generations.csv":
            continue
        if has_all_trained and path.name.startswith("trained_metrics_seed"):
            continue
        try:
            df = pd.read_csv(path)
            if not df.empty:
                df["source_file"] = path.name
                metric_frames.append(df)
        except Exception:
            pass
    metrics = pd.concat(metric_frames, ignore_index=True) if metric_frames else pd.DataFrame()
    def read_optional_csv(path: Path) -> pd.DataFrame:
        if not path.exists() or path.stat().st_size == 0:
            return pd.DataFrame()
        try:
            return pd.read_csv(path)
        except pd.errors.EmptyDataError:
            return pd.DataFrame()

    train = read_optional_csv(run_dir / "training_log.csv")
    cot = read_optional_csv(run_dir / "cot_generations.csv")
    return metrics, train, cot


