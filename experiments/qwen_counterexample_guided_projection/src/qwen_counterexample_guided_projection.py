#!/usr/bin/env python3
"""Counterexample-guided consistency projection for text transformations.

The experiment treats row-level model outputs as noisy supervision for a
task-local deterministic transducer. For each task:

1. Split public examples into train and held-out rows.
2. Enumerate train-consistent candidate expressions.
3. Generate counterexample-style probe inputs from the training inputs.
4. Optionally ask Qwen to label those probes.
5. Select a program with a noise-aware probe objective.
6. Evaluate the selected program on held-out rows.

All reports generated by this script are standalone and do not depend on prior
experiment text.
"""

