@dataclass(frozen=True)
class Example:
    inputs: Tuple[str, ...]
    output: str


@dataclass(frozen=True)
class Task:
    task_id: str
    family: str
    synthetic: bool
    features: Tuple[str, ...]
    examples: Tuple[Example, ...]
    source_path: str


@dataclass(frozen=True)
class Expr:
    code: str
    func: Callable[[Tuple[str, ...]], Optional[str]]
    depth: int
    tier: str

    def eval_one(self, row: Tuple[str, ...]) -> Optional[str]:
        try:
            value = self.func(row)
        except Exception:
            return None
        if value is None:
            return None
        return str(value)

    def eval_many(self, rows: Sequence[Tuple[str, ...]]) -> Optional[Tuple[str, ...]]:
        vals: List[str] = []
        for row in rows:
            v = self.eval_one(row)
            if v is None:
                return None
            vals.append(v)
        return tuple(vals)


