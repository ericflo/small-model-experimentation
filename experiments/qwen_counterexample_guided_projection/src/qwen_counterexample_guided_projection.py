def plot_candidate_availability(task_df: pd.DataFrame) -> None:
    sub = task_df[task_df["method"].eq("train_only")].copy()
    if sub.empty:
        return
    bins = ["0", "1-4", "5-24", "25-99", "100+"]
    def bucket(n: Any) -> str:
        n = int(n)
        if n == 0:
            return "0"
        if n < 5:
            return "1-4"
        if n < 25:
            return "5-24"
        if n < 100:
            return "25-99"
        return "100+"
    vals = sub["candidate_count"].map(bucket).value_counts().reindex(bins).fillna(0)
    fig, ax = plt.subplots(figsize=(7.5, 4.5))
    ax.bar(vals.index, vals.values, color="#7c3aed")
    ax.set_ylabel("Tasks")
    ax.set_title("Train-consistent candidate availability")
    ax.grid(True, axis="y", alpha=0.3)
    fig.tight_layout()
    fig.savefig(FIGURES / "candidate_availability.png", dpi=160)
    plt.close(fig)


def plot_gated_changes(task_df: pd.DataFrame) -> None:
    direct = task_df[task_df["method"].eq("direct_row_by_row")][["task_id", "full_task_exact", "row_exact"]].rename(
        columns={"full_task_exact": "direct_full", "row_exact": "direct_row"}
    )
    gated = task_df[task_df["method"].eq("qwen_projection_gated_direct")][
        ["task_id", "full_task_exact", "row_exact", "program"]
    ].rename(columns={"full_task_exact": "gated_full", "row_exact": "gated_row"})
    if direct.empty or gated.empty:
        return
    merged = direct.merge(gated, on="task_id")
    changed = merged["program"].fillna("").ne("")
    categories = {
        "fallback": int((~changed).sum()),
        "changed_helped": int((changed & (merged["gated_full"].astype(float) > merged["direct_full"].astype(float))).sum()),
        "changed_tied": int((changed & (merged["gated_full"].astype(float) == merged["direct_full"].astype(float))).sum()),
        "changed_hurt": int((changed & (merged["gated_full"].astype(float) < merged["direct_full"].astype(float))).sum()),
    }
    fig, ax = plt.subplots(figsize=(7.5, 4.5))
    ax.bar(categories.keys(), categories.values(), color=["#9ca3af", "#059669", "#2563eb", "#dc2626"])
    ax.set_ylabel("Tasks")
    ax.set_title("Gated projection decisions")
    ax.tick_params(axis="x", rotation=20)
    ax.grid(True, axis="y", alpha=0.3)
    fig.tight_layout()
    fig.savefig(FIGURES / "gated_decisions.png", dpi=160)
    plt.close(fig)


