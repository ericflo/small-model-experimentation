def mirror_benchmark() -> None:
    """Avoid writing into another experiment's large-artifact tree."""
    if BENCH_ROOT.exists():
        return
    if not SOURCE_BENCH_ROOT.exists():
        raise FileNotFoundError(f"Missing benchmark source: {SOURCE_BENCH_ROOT}")
    # Symlink keeps large data separate without duplicating it.
    LARGE_ROOT.mkdir(parents=True, exist_ok=True)
    BENCH_ROOT.symlink_to(SOURCE_BENCH_ROOT, target_is_directory=True)


def clean(s: Any) -> str:
    return re.sub(r"\s+", " ", "" if s is None else str(s)).strip()


def render_inputs(vals: Sequence[str]) -> str:
    if len(vals) == 1:
        return vals[0]
    return " | ".join(f"col{i}={v}" for i, v in enumerate(vals))


def title_words(s: Any) -> str:
    return " ".join(w.capitalize() for w in re.findall(r"[A-Za-z0-9]+", clean(s)))


def only_digits(s: Any) -> str:
    return re.sub(r"\D+", "", "" if s is None else str(s))


def only_alpha_space(s: Any) -> str:
    return clean(re.sub(r"[^A-Za-z]+", " ", "" if s is None else str(s)))


def only_alnum(s: Any) -> str:
    return re.sub(r"[^A-Za-z0-9]+", "", "" if s is None else str(s))


def initials(s: Any, sep: str = "") -> str:
    return sep.join(w[0].upper() for w in re.findall(r"[A-Za-z]+", str(s)))


def first_last_initials(s: Any, sep: str = " ") -> Optional[str]:
    words = re.findall(r"[A-Za-z]+", str(s))
    if len(words) < 2:
        return None
    return words[0][0].upper() + sep + words[-1][0].upper()


def parse_float(s: Any) -> Optional[float]:
    text = clean(s).replace("$", "").replace(",", "").replace("%", "")
    if not text:
        return None
    try:
        return float(text)
    except ValueError:
        m = re.search(r"-?\d+(?:\.\d+)?", text)
        return None if not m else float(m.group(0))


def fmt_int(v: Optional[float]) -> Optional[str]:
    if v is None:
        return None
    if abs(v - int(v)) < 1e-9:
        return str(int(v))
    return str(v)


DATE_PATTERNS = [
    (re.compile(r"\b(\d{4})-(\d{1,2})-(\d{1,2})\b"), ("Y", "M", "D")),
    (re.compile(r"\b(\d{1,2})/(\d{1,2})/(\d{2,4})\b"), ("M", "D", "Y")),
    (re.compile(r"\b(\d{1,2})-(\d{1,2})-(\d{2,4})\b"), ("M", "D", "Y")),
]
MONTHS = {
    "jan": 1,
    "january": 1,
    "feb": 2,
    "february": 2,
    "mar": 3,
    "march": 3,
    "apr": 4,
    "april": 4,
    "may": 5,
    "jun": 6,
    "june": 6,
    "jul": 7,
    "july": 7,
    "aug": 8,
    "august": 8,
    "sep": 9,
    "sept": 9,
    "september": 9,
    "oct": 10,
    "october": 10,
    "nov": 11,
    "november": 11,
    "dec": 12,
    "december": 12,
}


def date_parts(s: Any) -> Optional[Tuple[int, int, int]]:
    text = clean(s)
    for rgx, order in DATE_PATTERNS:
        m = rgx.search(text)
        if m:
            vals = [int(x) for x in m.groups()]
            parts = dict(zip(order, vals))
            year = parts["Y"] + (2000 if parts["Y"] < 100 else 0)
            return year, parts["M"], parts["D"]
    m = re.search(r"\b([A-Za-z]{3,9})\s+(\d{1,2}),?\s+(\d{4})\b", text)
    if m and m.group(1).lower() in MONTHS:
        return int(m.group(3)), MONTHS[m.group(1).lower()], int(m.group(2))
    m = re.search(r"\b(\d{1,2})\s+([A-Za-z]{3,9})\s+(\d{4})\b", text)
    if m and m.group(2).lower() in MONTHS:
        return int(m.group(3)), MONTHS[m.group(2).lower()], int(m.group(1))
    return None


def time_parts(s: Any) -> Optional[Tuple[int, int, int]]:
    m = re.search(r"\b(\d{1,2}):(\d{2})(?::(\d{2}))?\b", str(s))
    if not m:
        return None
    return int(m.group(1)), int(m.group(2)), int(m.group(3) or 0)


def nth_field(text: str, sep: str, idx: int) -> Optional[str]:
    parts = [p.strip() for p in str(text).split(sep)]
    if not parts:
        return None
    j = idx if idx >= 0 else len(parts) + idx
    if j < 0 or j >= len(parts):
        return None
    return parts[j]


def regex_group(text: str, pattern: str, idx: int = 1) -> Optional[str]:
    m = re.search(pattern, str(text))
    if not m:
        return None
    return m.group(idx)


def literal_exprs(outputs: Sequence[str]) -> List[Expr]:
    counts = Counter(outputs)
    vals = [x for x, _ in counts.most_common(4)]
    return [Expr(f"const({v!r})", lambda row, val=v: val, 0, "literal") for v in vals]


def source_exprs(num_cols: int) -> List[Expr]:
    exprs: List[Expr] = []
    for i in range(num_cols):
        prefix = f"COL{i}"
        exprs.extend(
            [
                Expr(prefix, lambda row, j=i: row[j] if j < len(row) else "", 0, "core"),
                Expr(f"strip({prefix})", lambda row, j=i: clean(row[j] if j < len(row) else ""), 1, "core"),
                Expr(f"lower({prefix})", lambda row, j=i: clean(row[j] if j < len(row) else "").lower(), 1, "core"),
                Expr(f"upper({prefix})", lambda row, j=i: clean(row[j] if j < len(row) else "").upper(), 1, "core"),
                Expr(f"title({prefix})", lambda row, j=i: title_words(row[j] if j < len(row) else ""), 1, "core"),
                Expr(f"digits({prefix})", lambda row, j=i: only_digits(row[j] if j < len(row) else ""), 1, "core"),
                Expr(f"alpha({prefix})", lambda row, j=i: only_alpha_space(row[j] if j < len(row) else ""), 1, "core"),
                Expr(f"alnum({prefix})", lambda row, j=i: only_alnum(row[j] if j < len(row) else ""), 1, "core"),
                Expr(f"initials({prefix})", lambda row, j=i: initials(row[j] if j < len(row) else ""), 1, "core"),
                Expr(f"initials_sp({prefix})", lambda row, j=i: initials(row[j] if j < len(row) else "", " "), 1, "core"),
                Expr(f"first_last_initials({prefix})", lambda row, j=i: first_last_initials(row[j] if j < len(row) else ""), 1, "core"),
            ]
        )
        seps = [" ", ",", "-", "/", "\\", "_", ":", ";", "|", ".", "\n", "\t", "(", ")", "@", "#"]
        for sep in seps:
            label = {" ": "space", "\n": "nl", "\t": "tab", "\\": "bslash"}.get(sep, sep)
            for idx in [0, 1, 2, 3, 4, 5, -1, -2, -3]:
                exprs.append(
                    Expr(
                        f"field[{label},{idx}]({prefix})",
                        lambda row, j=i, s=sep, k=idx: nth_field(row[j] if j < len(row) else "", s, k),
                        1,
                        "field",
                    )
                )
        regexes = [
            ("first_digits", r"(\d+)", 1),
            ("last_digits", r".*(\d+)", 1),
            ("first_word", r"([A-Za-z]+)", 1),
            ("last_word", r".*\b([A-Za-z]+)\b", 1),
            ("first_alnum", r"([A-Za-z0-9]+)", 1),
            ("last_alnum", r".*\b([A-Za-z0-9]+)\b", 1),
            ("email_user", r"([A-Za-z0-9._%+\-]+)@[A-Za-z0-9.\-]+", 1),
            ("email_domain", r"[A-Za-z0-9._%+\-]+@([A-Za-z0-9.\-]+)", 1),
            ("paren", r"\(([^()]*)\)", 1),
            ("bracket", r"\[([^\[\]]*)\]", 1),
            ("zip5", r"\b(\d{5})(?:-\d{4})?\b", 1),
            ("state_zip", r"\b([A-Z]{2})\s+\d{5}(?:-\d{4})?\b", 1),
            ("url_domain", r"^(?:[A-Za-z]+://)?([^/]+)", 1),
            ("file_ext", r"\.([A-Za-z0-9]+)$", 1),
            ("file_stem", r"([^/\\]+?)(?:\.[A-Za-z0-9]+)?$", 1),
        ]
        for name, pattern, group in regexes:
            exprs.append(Expr(f"{name}({prefix})", lambda row, j=i, p=pattern, g=group: regex_group(row[j] if j < len(row) else "", p, g), 1, "regex"))
        exprs.extend(
            [
                Expr(f"date_year({prefix})", lambda row, j=i: None if date_parts(row[j] if j < len(row) else "") is None else str(date_parts(row[j])[0]), 1, "date"),
                Expr(f"date_month({prefix})", lambda row, j=i: None if date_parts(row[j] if j < len(row) else "") is None else str(date_parts(row[j])[1]), 1, "date"),
                Expr(f"date_month2({prefix})", lambda row, j=i: None if date_parts(row[j] if j < len(row) else "") is None else f"{date_parts(row[j])[1]:02d}", 1, "date"),
                Expr(f"date_day({prefix})", lambda row, j=i: None if date_parts(row[j] if j < len(row) else "") is None else str(date_parts(row[j])[2]), 1, "date"),
                Expr(f"date_day2({prefix})", lambda row, j=i: None if date_parts(row[j] if j < len(row) else "") is None else f"{date_parts(row[j])[2]:02d}", 1, "date"),
                Expr(f"date_iso({prefix})", lambda row, j=i: None if date_parts(row[j] if j < len(row) else "") is None else f"{date_parts(row[j])[0]:04d}-{date_parts(row[j])[1]:02d}-{date_parts(row[j])[2]:02d}", 1, "date"),
                Expr(f"time_hour({prefix})", lambda row, j=i: None if time_parts(row[j] if j < len(row) else "") is None else str(time_parts(row[j])[0]), 1, "time"),
                Expr(f"time_hour2({prefix})", lambda row, j=i: None if time_parts(row[j] if j < len(row) else "") is None else f"{time_parts(row[j])[0]:02d}", 1, "time"),
                Expr(f"time_minute({prefix})", lambda row, j=i: None if time_parts(row[j] if j < len(row) else "") is None else str(time_parts(row[j])[1]), 1, "time"),
                Expr(f"number_int({prefix})", lambda row, j=i: fmt_int(parse_float(row[j] if j < len(row) else "")), 1, "number"),
                Expr(f"number_2dp({prefix})", lambda row, j=i: None if parse_float(row[j] if j < len(row) else "") is None else f"{parse_float(row[j]):.2f}", 1, "number"),
            ]
        )
    return exprs


def wrappers(exprs: List[Expr]) -> List[Expr]:
    out = list(exprs)
    transforms = [
        ("strip", lambda x: clean(x)),
        ("lower", lambda x: clean(x).lower()),
        ("upper", lambda x: clean(x).upper()),
        ("title", title_words),
        ("digits", only_digits),
        ("alnum", only_alnum),
        ("alpha", only_alpha_space),
    ]
    for e in exprs:
        if e.depth > 1:
            continue
        for name, fn in transforms:
            out.append(Expr(f"{name}({e.code})", lambda row, ex=e, f=fn: None if ex.eval_one(row) is None else f(ex.eval_one(row)), e.depth + 1, "wrap"))
    return out


def concat_exprs(exprs: List[Expr], train_rows: Sequence[Tuple[str, ...]], max_source: int = 120) -> List[Expr]:
    usable: List[Expr] = []
    for e in sorted(exprs, key=lambda x: (x.depth, len(x.code), x.code)):
        vals = e.eval_many(train_rows)
        if vals is None or any(v == "" for v in vals):
            continue
        # Keep both variable and constant pieces; constants matter for formatting.
        usable.append(e)
        if len(usable) >= max_source:
            break
    seps = ["", " ", ", ", "-", "/", "_", ":", ".", " - "]
    out: List[Expr] = []
    for a in usable:
        for b in usable:
            if a.code == b.code:
                continue
            for sep in seps:
                out.append(
                    Expr(
                        f"concat[{sep!r}]({a.code},{b.code})",
                        lambda row, x=a, y=b, s=sep: None if x.eval_one(row) is None or y.eval_one(row) is None else f"{x.eval_one(row)}{s}{y.eval_one(row)}",
                        max(a.depth, b.depth) + 1,
                        "concat",
                    )
                )
    return out


def slice_exprs(train_rows: Sequence[Tuple[str, ...]], train_y: Sequence[str]) -> List[Expr]:
    """Derive substring templates from train outputs as spans in train inputs."""
    out: List[Expr] = []
    if not train_rows:
        return out
    num_cols = max(len(r) for r in train_rows)
    for col in range(num_cols):
        starts: List[int] = []
        ends: List[int] = []
        ok = True
        for row, y in zip(train_rows, train_y):
            text = row[col] if col < len(row) else ""
            pos = text.find(y)
            if pos < 0 or y == "":
                ok = False
                break
            starts.append(pos)
            ends.append(pos + len(y))
        if ok and len(set(starts)) == 1:
            s = starts[0]
            out.append(Expr(f"slice_abs(COL{col},{s},len_train)", lambda row, j=col, st=s, ln=len(train_y[0]): (row[j] if j < len(row) else "")[st : st + ln], 2, "slice"))
        if ok and len(set(starts)) == 1 and len(set(len(y) for y in train_y)) == 1:
            s = starts[0]
            ln = len(train_y[0])
            out.append(Expr(f"slice_abs(COL{col},{s},{s+ln})", lambda row, j=col, st=s, l=ln: (row[j] if j < len(row) else "")[st : st + l], 2, "slice"))
        if ok and len(set(len((row[col] if col < len(row) else "")) - e for row, e in zip(train_rows, ends))) == 1:
            tail = len(train_rows[0][col]) - ends[0] if col < len(train_rows[0]) else 0
            out.append(Expr(f"slice_to_tail(COL{col},tail={tail})", lambda row, j=col, t=tail: (row[j] if j < len(row) else "")[:-t] if t else (row[j] if j < len(row) else ""), 2, "slice"))
    return out


def token_items(text: str, kind: str) -> List[str]:
    if kind == "word":
        return re.findall(r"[A-Za-z]+", text)
    if kind == "number":
        return re.findall(r"\d+", text)
    return re.findall(r"[A-Za-z0-9]+", text)


def token_span_exprs(train_rows: Sequence[Tuple[str, ...]], train_y: Sequence[str]) -> List[Expr]:
    """Learn constant token-span extraction patterns from examples."""
    out: List[Expr] = []
    if not train_rows:
        return out
    num_cols = max(len(r) for r in train_rows)
    joiners = [" ", "", "-", "_", "/", ".", ":"]
    for col in range(num_cols):
        for kind in ["word", "number", "alnum"]:
            row_tokens = [token_items(row[col] if col < len(row) else "", kind) for row in train_rows]
            for joiner in joiners:
                y_tokens = [token_items(y, kind) for y in train_y]
                spans: List[Tuple[int, int]] = []
                ok = True
                for toks, y_toks, y in zip(row_tokens, y_tokens, train_y):
                    if not toks or not y_toks:
                        ok = False
                        break
                    found = None
                    for s in range(len(toks)):
                        for e in range(s + 1, len(toks) + 1):
                            if joiner.join(toks[s:e]) == y:
                                found = (s, e)
                                break
                        if found:
                            break
                    if found is None:
                        ok = False
                        break
                    spans.append(found)
                if not ok:
                    continue
                starts = {s for s, _ in spans}
                lens = {e - s for s, e in spans}
                end_offsets = {len(toks) - e for toks, (_, e) in zip(row_tokens, spans)}
                if len(starts) == 1 and len(lens) == 1:
                    s = next(iter(starts))
                    ln = next(iter(lens))
                    out.append(
                        Expr(
                            f"{kind}_span_abs(COL{col},{s},{ln},join={joiner!r})",
                            lambda row, j=col, k=kind, st=s, l=ln, sep=joiner: sep.join(token_items(row[j] if j < len(row) else "", k)[st : st + l]),
                            2,
                            "token_span",
                        )
                    )
                if len(end_offsets) == 1 and len(lens) == 1:
                    off = next(iter(end_offsets))
                    ln = next(iter(lens))
                    out.append(
                        Expr(
                            f"{kind}_span_from_end(COL{col},tail={off},len={ln},join={joiner!r})",
                            lambda row, j=col, k=kind, t=off, l=ln, sep=joiner: sep.join(token_items(row[j] if j < len(row) else "", k)[max(0, len(token_items(row[j] if j < len(row) else "", k)) - t - l) : len(token_items(row[j] if j < len(row) else "", k)) - t]),
                            2,
                            "token_span",
                        )
                    )
    return out


def affix_exprs(train_rows: Sequence[Tuple[str, ...]], train_y: Sequence[str]) -> List[Expr]:
    """Learn simple affix additions around an inner expression."""
    if not train_rows:
        return []
    num_cols = max(len(r) for r in train_rows)
    bases = wrappers(source_exprs(num_cols)) + token_span_exprs(train_rows, train_y)
    out: List[Expr] = []
    for base in bases:
        vals = base.eval_many(train_rows)
        if vals is None:
            continue
        prefixes: List[str] = []
        suffixes: List[str] = []
        ok = True
        for v, y in zip(vals, train_y):
            if not y.startswith(v) and not y.endswith(v):
                ok = False
                break
            if y.startswith(v):
                prefixes.append("")
                suffixes.append(y[len(v) :])
            elif y.endswith(v):
                prefixes.append(y[: -len(v)] if v else y)
                suffixes.append("")
        if ok and len(set(prefixes)) == 1 and len(set(suffixes)) == 1:
            pre, suf = prefixes[0], suffixes[0]
            if pre or suf:
                out.append(
                    Expr(
                        f"affix({pre!r},{base.code},{suf!r})",
                        lambda row, b=base, p=pre, s=suf: None if b.eval_one(row) is None else f"{p}{b.eval_one(row)}{s}",
                        base.depth + 1,
                        "affix",
                    )
                )
    return out


def candidate_exprs(num_cols: int, train_rows: Sequence[Tuple[str, ...]], train_y: Sequence[str]) -> List[Expr]:
    base = source_exprs(num_cols) + literal_exprs(train_y)
    exprs = wrappers(base)
    exprs += slice_exprs(train_rows, train_y)
    exprs += token_span_exprs(train_rows, train_y)
    exprs += affix_exprs(train_rows, train_y)
    exprs += concat_exprs(exprs, train_rows)
    seen: set[str] = set()
    dedup: List[Expr] = []
    for e in sorted(exprs, key=lambda x: (x.depth, len(x.code), x.code)):
        if e.code in seen:
            continue
        seen.add(e.code)
        dedup.append(e)
    return dedup


def load_tasks(limit: Optional[int] = None, min_examples: int = 5) -> List[Task]:
    tasks: List[Task] = []
    for d in sorted(TRANSFORM_ROOT.iterdir()):
        if not d.is_dir() or not (d / "spec.json").exists():
            continue
        spec = json.loads((d / "spec.json").read_text())
        meta = json.loads((d / "meta.json").read_text()) if (d / "meta.json").exists() else {}
        examples: List[Example] = []
        for ex in spec.get("Examples", []):
            examples.append(Example(tuple(str(x) for x in ex.get("Input", [])), str(ex.get("Output", ""))))
        if len(examples) < min_examples:
            continue
        tasks.append(
            Task(
                task_id=d.name,
                family=d.name.split(".", 1)[0],
                synthetic=bool(meta.get("Synthetic", False)),
                features=tuple(str(x) for x in meta.get("Features", [])),
                examples=tuple(examples),
                source_path=str(d),
            )
        )
        if limit and len(tasks) >= limit:
            break
    return tasks


def split_examples(task: Task, train_n: int, heldout_cap: int) -> Tuple[List[Example], List[Example]]:
    n = len(task.examples)
    k = min(train_n, max(2, n // 2))
    train = list(task.examples[:k])
    test = list(task.examples[k:])
    if heldout_cap > 0:
        test = test[:heldout_cap]
    return train, test


def choose_tasks(tasks: List[Task], limit: int, seed: int, train_n: int, heldout_cap: int, min_heldout: int) -> List[Task]:
    eligible = []
    for t in tasks:
        _, test = split_examples(t, train_n, heldout_cap)
        if len(test) >= min_heldout:
            eligible.append(t)
    rng = random.Random(seed)
    if limit and limit < len(eligible):
        chosen = rng.sample(eligible, limit)
    else:
        chosen = eligible
    return sorted(chosen, key=lambda t: t.task_id)


def infer_token_pool(values: Sequence[str]) -> Dict[str, List[str]]:
    words: List[str] = []
    nums: List[str] = []
    alnums: List[str] = []
    seps = set()
    for v in values:
        words.extend(re.findall(r"[A-Za-z]+", v))
        nums.extend(re.findall(r"\d+", v))
        alnums.extend(re.findall(r"[A-Za-z0-9]+", v))
        seps.update(ch for ch in v if not ch.isalnum())
    return {
        "words": sorted(set(words)) or ["Alpha", "Beta", "Gamma", "Delta"],
        "nums": sorted(set(nums)) or ["12", "34", "2026", "07"],
        "alnums": sorted(set(alnums)) or ["A12", "B34", "X99"],
        "seps": sorted(seps) or [" ", "-", "_", ",", "/", ":"],
    }


def mutate_text(text: str, pool: Dict[str, List[str]], rng: random.Random) -> str:
    if not text:
        return rng.choice(pool["words"])
    options: List[str] = []
    # Replace one word or number with another token of the same coarse kind.
    word_matches = list(re.finditer(r"[A-Za-z]+", text))
    num_matches = list(re.finditer(r"\d+", text))
    if word_matches:
        m = rng.choice(word_matches)
        repl = rng.choice(pool["words"])
        options.append(text[: m.start()] + repl + text[m.end() :])
    if num_matches:
        m = rng.choice(num_matches)
        repl = rng.choice(pool["nums"])
        options.append(text[: m.start()] + repl + text[m.end() :])
    # Perturb casing or spacing.
    options.append(text.upper())
    options.append(text.lower())
    options.append(text.title())
    if " " in text:
        options.append(text.replace(" ", rng.choice(["_", "-", "  "])))
    if "," in text:
        parts = [p.strip() for p in text.split(",")]
        if len(parts) > 1:
            rng.shuffle(parts)
            options.append(", ".join(parts))
    # Splice a prefix/suffix from observed tokens.
    options.append(f"{rng.choice(pool['words'])}{rng.choice(pool['seps'])}{text}")
    options.append(f"{text}{rng.choice(pool['seps'])}{rng.choice(pool['nums'])}")
    return rng.choice([o for o in options if o is not None])


def generate_probes(task: Task, train: Sequence[Example], num_probes: int, seed: int) -> List[Tuple[str, ...]]:
    rng = random.Random((hash(task.task_id) & 0xFFFFFFFF) ^ seed)
    train_rows = [ex.inputs for ex in train]
    flat_values = [v for row in train_rows for v in row]
    pool = infer_token_pool(flat_values)
    probes: List[Tuple[str, ...]] = []
    seen = set(train_rows)
    num_cols = max(len(r) for r in train_rows)
    attempts = 0
    while len(probes) < num_probes and attempts < num_probes * 40:
        attempts += 1
        base = list(rng.choice(train_rows))
        while len(base) < num_cols:
            base.append("")
        j = rng.randrange(num_cols)
        base[j] = mutate_text(base[j], pool, rng)
        # Occasionally recombine columns from two train rows.
        if num_cols > 1 and rng.random() < 0.35:
            other = rng.choice(train_rows)
            k = rng.randrange(num_cols)
            if k < len(other):
                base[k] = other[k]
        probe = tuple(base[:num_cols])
        if probe not in seen:
            seen.add(probe)
            probes.append(probe)
    return probes


def train_matching_candidates(task: Task, train: Sequence[Example], max_candidates: int = 400) -> List[Expr]:
    train_rows = [e.inputs for e in train]
    train_y = tuple(e.output for e in train)
    num_cols = max(len(e.inputs) for e in task.examples)
    candidates = candidate_exprs(num_cols, train_rows, train_y)
    matches: List[Expr] = []
    seen_code: set[str] = set()
    for expr in candidates:
        sig = expr.eval_many(train_rows)
        if sig is None or sig != train_y:
            continue
        if expr.code in seen_code:
            continue
        seen_code.add(expr.code)
        matches.append(expr)
        if len(matches) >= max_candidates:
            break
    return sorted(matches, key=lambda e: (e.depth, len(e.code), e.code))


def select_train_only(candidates: Sequence[Expr]) -> Optional[Expr]:
    return min(candidates, key=lambda e: (e.depth, len(e.code), e.code), default=None)


def qwen_score(
    expr: Expr,
    probes: Sequence[Tuple[str, ...]],
    labels: Sequence[str],
    complexity_penalty: float,
) -> Tuple[float, int, int]:
    agree = 0
    valid = 0
    for row, lab in zip(probes, labels):
        pred = expr.eval_one(row)
        if pred is None:
            continue
        valid += 1
        if clean(pred) == clean(lab):
            agree += 1
    score = agree - complexity_penalty * expr.depth - 0.002 * len(expr.code)
    return score, agree, valid


def select_with_probe_labels(
    candidates: Sequence[Expr],
    probes: Sequence[Tuple[str, ...]],
    labels: Sequence[str],
    complexity_penalty: float,
) -> Tuple[Optional[Expr], Dict[str, Any]]:
    if not candidates:
        return None, {"probe_agree": 0, "probe_valid": 0, "probe_count": len(labels), "score": float("-inf")}
    scored = []
    for expr in candidates:
        score, agree, valid = qwen_score(expr, probes, labels, complexity_penalty)
        scored.append((score, agree, valid, expr))
    scored.sort(key=lambda t: (-t[0], -t[1], t[3].depth, len(t[3].code), t[3].code))
    score, agree, valid, expr = scored[0]
    return expr, {"probe_agree": agree, "probe_valid": valid, "probe_count": len(labels), "score": score}


def load_existing_predictions(path: Optional[Path]) -> Dict[Tuple[str, str, int], str]:
    if path is None or not path.exists():
        return {}
    df = pd.read_csv(path)
    out: Dict[Tuple[str, str, int], str] = {}
    for _, r in df.iterrows():
        out[(str(r["task_id"]), str(r["method"]), int(r["row_index"]))] = "" if pd.isna(r["prediction"]) else str(r["prediction"])
    return out


def direct_baseline_from_existing(task: Task, test: Sequence[Example], existing: Dict[Tuple[str, str, int], str], method: str) -> List[str]:
    preds = []
    for row_idx, _ in enumerate(test, start=1):
        preds.append(existing.get((task.task_id, method, row_idx), ""))
    return preds


def clean_prediction(text: str) -> str:
    text = re.sub(r"(?is)<think>.*?</think>", "", text).strip()
    if text.lower().startswith("output:"):
        text = text.split(":", 1)[1].strip()
    first = text.splitlines()[0].strip() if text else ""
    if (first.startswith('"') and first.endswith('"')) or (first.startswith("'") and first.endswith("'")):
        first = first[1:-1].strip()
    return first


def row_prompt(train_pairs: Sequence[Tuple[Tuple[str, ...], str]], query: Tuple[str, ...]) -> str:
    lines = [
        "Infer the text transformation from the examples.",
        "Return only the transformed output for the query. Do not explain.",
        "",
        "Examples:",
    ]
    for inp, out in train_pairs:
        lines.append(f"Input: {render_inputs(inp)}")
        lines.append(f"Output: {out}")
    lines.extend(["", "Query:", f"Input: {render_inputs(query)}", "Output:"])
    return "\n".join(lines)


def load_qwen() -> Tuple[Any, Any]:
    import torch
    from transformers import AutoModelForCausalLM, AutoTokenizer, BitsAndBytesConfig

    tok = AutoTokenizer.from_pretrained(MODEL_NAME, cache_dir=str(CACHE_DIR), trust_remote_code=True)
    if tok.pad_token is None:
        tok.pad_token = tok.eos_token
    tok.padding_side = "left"
    bnb = BitsAndBytesConfig(
        load_in_4bit=True,
        bnb_4bit_quant_type="nf4",
        bnb_4bit_use_double_quant=True,
        bnb_4bit_compute_dtype=torch.bfloat16,
    )
    model = AutoModelForCausalLM.from_pretrained(
        MODEL_NAME,
        cache_dir=str(CACHE_DIR),
        trust_remote_code=True,
        quantization_config=bnb,
        device_map="auto",
    )
    model.eval()
    return tok, model


def generate_text(tok: Any, model: Any, system: str, user: str, max_new_tokens: int) -> str:
    import torch

    messages = [{"role": "system", "content": system}, {"role": "user", "content": user}]
    try:
        rendered = tok.apply_chat_template(messages, tokenize=False, add_generation_prompt=True, enable_thinking=False)
    except TypeError:
        rendered = tok.apply_chat_template(messages, tokenize=False, add_generation_prompt=True)
    enc = tok(rendered, return_tensors="pt").to(model.device)
    with torch.inference_mode():
        out = model.generate(
            **enc,
            do_sample=False,
            max_new_tokens=max_new_tokens,
            pad_token_id=tok.pad_token_id,
            eos_token_id=tok.eos_token_id,
        )
    return tok.decode(out[0, enc["input_ids"].shape[1] :], skip_special_tokens=True).strip()


def label_probes_qwen(
    tasks: Sequence[Task],
    train_map: Dict[str, List[Example]],
    probe_map: Dict[str, List[Tuple[str, ...]]],
    cache_path: Path,
    max_new_tokens: int,
    use_qwen: bool,
) -> Dict[Tuple[str, int], str]:
    labels: Dict[Tuple[str, int], str] = {}
    if cache_path.exists():
        with cache_path.open() as f:
            for row in csv.DictReader(f):
                labels[(row["task_id"], int(row["probe_index"]))] = row["label"]
    missing = []
    for task in tasks:
        for i, probe in enumerate(probe_map[task.task_id]):
            if (task.task_id, i) not in labels:
                missing.append((task, i, probe))
    if not missing or not use_qwen:
        return labels
    tok, model = load_qwen()
    cache_path.parent.mkdir(parents=True, exist_ok=True)
    file_exists = cache_path.exists()
    with cache_path.open("a", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=["task_id", "probe_index", "probe", "label", "raw"])
        if not file_exists:
            writer.writeheader()
        for n, (task, i, probe) in enumerate(missing, start=1):
            train_pairs = [(e.inputs, e.output) for e in train_map[task.task_id]]
            raw = generate_text(
                tok,
                model,
                "You transform text exactly from examples.",
                row_prompt(train_pairs, probe),
                max_new_tokens=max_new_tokens,
            )
            label = clean_prediction(raw)
            labels[(task.task_id, i)] = label
            writer.writerow({"task_id": task.task_id, "probe_index": i, "probe": json.dumps(probe), "label": label, "raw": raw})
            f.flush()
            if n == 1 or n % 25 == 0 or n == len(missing):
                print(f"labeled probes {n}/{len(missing)}", flush=True)
    return labels


def evaluate_predictions(preds: Sequence[str], targets: Sequence[str]) -> Tuple[float, bool]:
    exacts = [clean(p) == clean(t) for p, t in zip(preds, targets)]
    return (sum(exacts) / len(exacts) if exacts else 0.0), bool(exacts and all(exacts))


def task_family_features(task: Task) -> str:
    return ",".join(task.features)


