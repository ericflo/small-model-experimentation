def aggregate_summary(task_df: pd.DataFrame) -> pd.DataFrame:
    return (
        task_df.groupby("method", as_index=False)
        .agg(
            tasks=("task_id", "count"),
            row_exact=("row_exact", "mean"),
            full_task_exact=("full_task_exact", "mean"),
            median_candidates=("candidate_count", "median"),
            median_probe_agreement=("probe_agreement_rate", "median"),
        )
        .sort_values("full_task_exact", ascending=False)
    )


def by_family(task_df: pd.DataFrame) -> pd.DataFrame:
    return (
        task_df.groupby(["method", "family"], as_index=False)
        .agg(tasks=("task_id", "count"), row_exact=("row_exact", "mean"), full_task_exact=("full_task_exact", "mean"))
        .sort_values(["method", "family"])
    )


def pct(x: Any) -> str:
    if x is None or pd.isna(x):
        return ""
    return f"{100 * float(x):.1f}%"


def md_table(df: pd.DataFrame, cols: Sequence[str], max_rows: int = 80) -> str:
    if df.empty:
        return "_No rows._"
    view = df[list(cols)].head(max_rows).copy()
    for c in view.columns:
        if view[c].dtype.kind in "fc":
            if "exact" in c or "rate" in c or "agreement" in c:
                view[c] = view[c].map(pct)
            else:
                view[c] = view[c].map(lambda v: "" if pd.isna(v) else f"{v:.2f}")
    header = "|" + "|".join(cols) + "|"
    sep = "|" + "|".join(["---"] * len(cols)) + "|"
