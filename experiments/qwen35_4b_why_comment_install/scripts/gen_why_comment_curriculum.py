#!/usr/bin/env python3
"""Truth-audited synthetic curriculum: WHY-annotated correct code.

The FOURTH designed curriculum bet of the cognitive-core coding program
(lifecycle 35) and the FIRST of the owner-specified WHY-not-WHAT family: teach
the 4B WHY a correct answer is correct (its causal/generating structure), not
just WHAT the answer is. This variant puts the rationale as INLINE ``#WHY:``
code comments bound to the exact line each explains, leaving the think channel
essentially alone.

Why the inline-comment variant is the CLEANEST test of the WHY hypothesis:
code comments are INERT to execution grading (the eval runs the code and
ignores comments), so the hypothesis can be tested DIRECTLY with ZERO
annealing. We train the model to write richly-``#WHY:``-commented code and
evaluate pass@1 (comments ignored). If the CODE got better, the WHY-annotation
worked, unconfounded by any grader that reads the comments.

Each row is a self-contained ``spec -> correct-solution`` episode:

  (a) a CORRECT small synthetic Python function generated BY CONSTRUCTION from a
      parameterized family (so we KNOW the intended algorithm and the role of
      every line), together with its concrete ``assert`` tests, all of which
      PASS (verified by real CPython execution);
  (b) EACH meaningful line of the correct solution annotated with a trailing
      ``#WHY:`` comment giving the TRUE CAUSAL reason that line is correct — the
      constraint it satisfies, the invariant it maintains, the bound/edge it
      handles. The ``#WHY:`` text is emitted MECHANICALLY by the family
      generator (per line, the reason it chose that construct/bound/op), NOT
      free-form fabrication and NOT a teacher model;
  (c) prompt = a natural ``write a function`` framing (spec + signature + tests)
      matching how the eval prompts, WITHOUT any instruction to comment (so the
      WHY-writing behavior is the model's DEFAULT, and transfers to a plain
      HumanEval/MBPP prompt); think = MINIMAL (a single line — the rationale
      lives inline, never in the think channel); answer = the correct solution
      WITH the ``#WHY:`` comments.

Provenance: there is NO teacher model. The task and its correct solution are
built BY CONSTRUCTION (we build the problem and the solution, so we know why
each line is correct), and every pair is confirmed by REAL CPython execution.

Per-row truth audit (never ship an unverified row), all by real execution:

1. STRIP the ``#WHY:`` comments -> the resulting CLEAN code passes ALL the
   task's asserts (independently re-executed).
2. The COMMENTED code runs and passes ALL the same asserts IDENTICALLY (comments
   are inert to Python, verified rather than assumed).
3. The ``#WHY:`` marker is distinctive and mechanically strippable: stripping it
   reproduces the clean code, and no ``#WHY:`` survives in the clean code.
4. Every ``#WHY:`` comment is REAL causal rationale, not boilerplate: the
   comments VARY per line (all distinct within a row) and each references a
   line-specific token (a variable/constant that actually appears on that line).

Safety/termination: no imports, no I/O, restricted builtins, only bounded
for-loops (never ``while``), a per-call step cap that aborts and discards.
Determinism: construction seed (default 92450); the corpus is a pure function
of the seed. A MIXED, frozen difficulty schedule (short/medium/long, biased to
medium/long because the base already handles trivial code) drives the loop.

Output length: commented solutions are longer than plain code, so the receipt
records the character-length distribution and a conservative token estimate;
the report records the empirically measured max token count (well under the
4096 training cap — no truncation risk).

No annealing in this cell: the commented model IS the test. A comment-strip
anneal is a documented FUTURE follow-on, only if this clears the bar.
"""

